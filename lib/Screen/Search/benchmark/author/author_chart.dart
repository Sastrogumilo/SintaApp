            children: <Widget>[
              Text(
                title,
                style: Theme.of(context).textTheme.body2,
              ),
              Expanded(
                child: charts.PieChart(seriesList,
                  defaultRenderer: new charts.ArcRendererConfig(
                    arcWidth: 60,
